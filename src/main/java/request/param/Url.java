package request.param;

public class Url {

	private String homepageUrl;
	private Element loggedInCheckElement;

	public Url() {
		super();
	}

	public String getHomepageUrl() {
		return homepageUrl;
	}

	public void setHomepageUrl(String homepageUrl) {
		this.homepageUrl = homepageUrl;
	}

	public Element getLoggedInCheckElement() {
		return loggedInCheckElement;
	}

	public void setLoggedInCheckElement(Element loggedInCheckElement) {
		this.loggedInCheckElement = loggedInCheckElement;
	}

}
